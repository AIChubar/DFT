Map::Map()
{
    dirt = TextureManager::loadTexture("assets/yellow.png");

    loadMap(field);

    src.x = src.y = 0;
    src.w = dest.w = 32;
    src.h = dest.h = 32;

    dest.x = dest.y = 0;
}

